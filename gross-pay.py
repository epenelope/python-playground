#CALCULATE GROSS PAY
#the next two lines assign the names for user input strings:
hrs = input('How many hours did you work?')
rte = input('What is your hourly rate?')
#the next two lines convert strings of user input into floating point integers:
hours = float(hrs)
rate = float(rte)
#next, multiply hours by rate and display the product to the user:
print(hours*rate)



